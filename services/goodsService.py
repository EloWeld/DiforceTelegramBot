    @staticmethod
    def LevenshteinDistance(s1, s2) -> int:
        """
        –í—ã—á–∏—Å–ª—è–µ—Ç —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ –õ–µ–≤–µ–Ω—à—Ç–µ–π–Ω–∞ –º–µ–∂–¥—É —Å—Ç—Ä–æ–∫–∞–º–∏ `s1` –∏ `s2` –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –µ–≥–æ –∑–Ω–∞—á–µ–Ω–∏–µ.
        """
        return Levenshtein.distance(s1, s2)

